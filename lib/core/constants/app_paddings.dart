/// A class that defines common padding values for the app.
abstract class AppPaddings {
  AppPaddings._();

  /// A [p4] padding value (4.0).
  static const double p4 = 4.0;

  /// A [p8] padding value (8.0).
  static const double p8 = 8.0;

  /// A [p12] padding extra value (12.0).
  static const double p12 = 12.0;

  /// A [p16] padding value (16.0).
  static const double p16 = 16.0;

  /// A [p20] padding value (20.0).
  static const double p20 = 20.0;

  /// A [p24] padding value (24.0).
  static const double p24 = 24.0;

  /// A [p28] padding value (24.0).
  static const double p28 = 28.0;

  /// An [p32]-large padding value (32.0).
  static const double p32 = 32.0;

  /// An [p36]-large padding value (36.0).
  static const double p36 = 36.0;

  /// An [p64]-large padding value (64.0).
  static const double p64 = 64.0;
}
